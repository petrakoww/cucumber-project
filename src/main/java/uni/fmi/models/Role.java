package uni.fmi.models;

public class Role {
    private String role;
    private String owner;

    public Role(String role) {
        this.role = role;
        this.owner = owner;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
    public String getOwner() {
        return role;
    }

    public void setOwner(String owner) {
        this.owner = owner;
    }
}
